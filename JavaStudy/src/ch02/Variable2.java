package ch02;

public class Variable2 {
	public static void main(String[] args) {
		int num = 65;
		System.out.println( (char) num );
		int num2 = num + 25;
		System.out.println( (char) num2 );
		
		char ch = 'a';
		char ch2 = 'z';
		System.out.println( (int) ch );
		System.out.println( (int) ch2 );
	}
}
